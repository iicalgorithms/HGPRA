#!/usr/bin/env python3
"""Build the paper component-ablation CSV from completed formal logs."""

from __future__ import annotations

import argparse
import csv
import os
from decimal import Decimal, ROUND_HALF_UP
from typing import Dict, Iterable, List, Tuple


DATASET_LABELS = {
    "coauthor_cora": "Cora-CA",
    "citeseer": "Citeseer-CC",
}

VARIANT_LABELS = {
    "hgpra": "Full HGPRA",
    "without_pas": "w/o PaS",
    "without_pra": "w/o PRA",
    "without_kged": "w/o KGED",
}

FIELDNAMES = ["setting", "Cora-CA", "Citeseer-CC"]
SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
CODE_DIR = os.path.dirname(SCRIPT_DIR)


def _read_csv(path: str) -> List[Dict[str, str]]:
    with open(path, "r", encoding="utf-8", newline="") as handle:
        return list(csv.DictReader(handle))


def _write_csv(path: str, rows: Iterable[Dict[str, str]]) -> None:
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=FIELDNAMES)
        writer.writeheader()
        writer.writerows(rows)


def _format_accuracy(row: Dict[str, str]) -> str:
    if not row.get("test_acc_percent") or not row.get("test_std_percent"):
        raise ValueError(f"Missing accuracy/std in formal row: {row}")
    mean = Decimal(row["test_acc_percent"]).quantize(Decimal("0.01"), rounding=ROUND_HALF_UP)
    std = Decimal(row["test_std_percent"]).quantize(Decimal("0.01"), rounding=ROUND_HALF_UP)
    return f"{mean}+-{std}"


def build_rows(formal_rows: Iterable[Dict[str, str]], require_all: bool = False) -> List[Dict[str, str]]:
    lookup: Dict[Tuple[str, str], str] = {}
    for row in formal_rows:
        if row.get("method") and row.get("method") != "AllDeepSets":
            continue
        if row.get("ratio") != "0.03":
            continue
        dataset = DATASET_LABELS.get(row.get("dataset", ""))
        variant = VARIANT_LABELS.get(row.get("variant", ""))
        if row.get("preset") == "main" and row.get("variant") == "hgpra":
            variant = VARIANT_LABELS["hgpra"]
        elif row.get("preset") != "ablation":
            continue
        if not dataset or not variant:
            continue
        lookup[(variant, dataset)] = _format_accuracy(row)

    rows = []
    missing = []
    for variant in VARIANT_LABELS.values():
        output_row = {"setting": variant, "Cora-CA": "", "Citeseer-CC": ""}
        for dataset in DATASET_LABELS.values():
            value = lookup.get((variant, dataset), "")
            output_row[dataset] = value
            if not value:
                missing.append((variant, dataset))
        rows.append(output_row)

    if require_all and missing:
        formatted = ", ".join(f"{variant}/{dataset}" for variant, dataset in missing)
        raise ValueError(f"Formal ablation results are missing {len(missing)} cells: {formatted}")
    return rows


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--formal", required=True, help="CSV generated by collect_results.py.")
    parser.add_argument(
        "--out",
        default=os.path.join(CODE_DIR, "results", "paper", "component_ablation.csv"),
        help="Paper component-ablation CSV to write.",
    )
    parser.add_argument("--require-all", action="store_true")
    args = parser.parse_args()

    rows = build_rows(_read_csv(args.formal), require_all=args.require_all)
    _write_csv(args.out, rows)
    print(f"Wrote {len(rows)} ablation rows to {args.out}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
