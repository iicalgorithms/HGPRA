#!/usr/bin/env python3
"""Merge completed formal HGPRA rows into the paper main-performance CSV."""

from __future__ import annotations

import argparse
import csv
import os
from decimal import Decimal, ROUND_HALF_UP
from typing import Dict, Iterable, List, Tuple


DATASET_LABELS = {
    "coauthor_cora": "Cora-CA",
    "citeseer": "Citeseer-CC",
    "pubmed": "Pubmed-CC",
    "20newsW100": "20News",
    "ModelNet40": "ModelNet40",
}

RATIO_LABELS = {
    "0.03": "3.00%",
    "0.01": "1.00%",
    "0.005": "0.50%",
    "0.001": "0.10%",
    "0.0007": "0.07%",
    "0.0005": "0.05%",
    "0.003": "0.30%",
}

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
CODE_DIR = os.path.dirname(SCRIPT_DIR)


def _read_csv(path: str) -> List[Dict[str, str]]:
    with open(path, "r", encoding="utf-8", newline="") as handle:
        return list(csv.DictReader(handle))


def _write_csv(path: str, rows: Iterable[Dict[str, str]], fieldnames: List[str]) -> None:
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def _ratio_label(value: str) -> str:
    if value in RATIO_LABELS:
        return RATIO_LABELS[value]
    return f"{float(value) * 100:.2f}%"


def _format_accuracy(row: Dict[str, str]) -> str:
    if not row.get("test_acc_percent") or not row.get("test_std_percent"):
        raise ValueError(f"Missing accuracy/std in formal row: {row}")
    mean = Decimal(row["test_acc_percent"]).quantize(Decimal("0.01"), rounding=ROUND_HALF_UP)
    std = Decimal(row["test_std_percent"]).quantize(Decimal("0.01"), rounding=ROUND_HALF_UP)
    return f"{mean}+-{std}"


def formal_lookup(rows: Iterable[Dict[str, str]]) -> Dict[Tuple[str, str], str]:
    lookup: Dict[Tuple[str, str], str] = {}
    for row in rows:
        if row.get("preset") != "main" or row.get("variant") != "hgpra":
            continue
        if row.get("method") and row.get("method") != "AllDeepSets":
            continue
        dataset = DATASET_LABELS.get(row.get("dataset", ""))
        if dataset is None:
            continue
        key = (dataset, _ratio_label(row["ratio"]))
        lookup[key] = _format_accuracy(row)
    return lookup


def merge_rows(paper_rows: List[Dict[str, str]], formal_rows: Iterable[Dict[str, str]], require_all: bool) -> int:
    lookup = formal_lookup(formal_rows)
    updated = 0
    missing = []
    for row in paper_rows:
        key = (row["dataset"], row["ratio"])
        if key in lookup:
            row["HGPRA"] = lookup[key]
            updated += 1
        else:
            missing.append(key)
    if require_all and missing:
        formatted = ", ".join(f"{dataset}/{ratio}" for dataset, ratio in missing)
        raise ValueError(f"Formal results are missing {len(missing)} paper rows: {formatted}")
    return updated


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--formal", required=True, help="CSV generated by collect_results.py.")
    parser.add_argument(
        "--paper",
        default=os.path.join(CODE_DIR, "results", "paper", "main_performance.csv"),
        help="Paper main-performance CSV to update.",
    )
    parser.add_argument("--out", default=None, help="Output path. Defaults to overwriting --paper.")
    parser.add_argument("--require-all", action="store_true")
    args = parser.parse_args()

    paper_rows = _read_csv(args.paper)
    fieldnames = list(paper_rows[0].keys()) if paper_rows else []
    updated = merge_rows(paper_rows, _read_csv(args.formal), require_all=args.require_all)
    out_path = args.out or args.paper
    _write_csv(out_path, paper_rows, fieldnames)
    print(f"Updated {updated} HGPRA rows in {out_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
