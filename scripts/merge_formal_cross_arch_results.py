#!/usr/bin/env python3
"""Merge completed formal HGPRA transfer rows into the paper cross-architecture CSV."""

from __future__ import annotations

import argparse
import csv
import os
from decimal import Decimal, ROUND_HALF_UP
from typing import Dict, Iterable, List, Tuple


DATASET_LABELS = {
    "coauthor_cora": "Cora-CA",
    "citeseer": "Citeseer-CC",
    "pubmed": "Pubmed-CC",
    "20newsW100": "20News",
    "ModelNet40": "ModelNet40",
}

RATIO_LABELS = {
    "0.03": "3%",
    "0.001": "0.1%",
    "0.01": "1%",
}

ARCH_COLUMNS = {
    "HyperGCN": "HyperGCN",
    "HGNN": "HGNN",
    "AllDeepSets": "AllDeepSets",
    "AllSetTransformer": "AllSetTransformer",
}

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
CODE_DIR = os.path.dirname(SCRIPT_DIR)


def _read_csv(path: str) -> List[Dict[str, str]]:
    with open(path, "r", encoding="utf-8", newline="") as handle:
        return list(csv.DictReader(handle))


def _write_csv(path: str, rows: Iterable[Dict[str, str]], fieldnames: List[str]) -> None:
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def _format_accuracy(row: Dict[str, str]) -> str:
    if not row.get("test_acc_percent"):
        raise ValueError(f"Missing accuracy in formal row: {row}")
    mean = Decimal(row["test_acc_percent"]).quantize(Decimal("0.01"), rounding=ROUND_HALF_UP)
    return str(mean)


def formal_lookup(rows: Iterable[Dict[str, str]]) -> Dict[Tuple[str, str, str], str]:
    lookup: Dict[Tuple[str, str, str], str] = {}
    for row in rows:
        if row.get("preset") != "main" or row.get("variant") != "hgpra":
            continue
        arch = ARCH_COLUMNS.get(row.get("method", ""))
        dataset = DATASET_LABELS.get(row.get("dataset", ""))
        ratio = RATIO_LABELS.get(row.get("ratio", ""))
        if not arch or not dataset or not ratio:
            continue
        lookup[(dataset, ratio, arch)] = _format_accuracy(row)
    return lookup


def merge_rows(paper_rows: List[Dict[str, str]], formal_rows: Iterable[Dict[str, str]], require_all: bool) -> int:
    lookup = formal_lookup(formal_rows)
    updated = 0
    missing = []
    for row in paper_rows:
        if row.get("method") != "HGPRA":
            continue
        for arch in ARCH_COLUMNS.values():
            key = (row["dataset"], row["ratio"], arch)
            if key in lookup:
                row[arch] = lookup[key]
                updated += 1
            else:
                missing.append(key)
    if require_all and missing:
        formatted = ", ".join(f"{dataset}/{ratio}/{arch}" for dataset, ratio, arch in missing)
        raise ValueError(f"Formal transfer results are missing {len(missing)} cells: {formatted}")
    return updated


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--formal", required=True, help="CSV generated by collect_results.py.")
    parser.add_argument(
        "--paper",
        default=os.path.join(CODE_DIR, "results", "paper", "cross_architecture.csv"),
        help="Paper cross-architecture CSV to update.",
    )
    parser.add_argument("--out", default=None, help="Output path. Defaults to overwriting --paper.")
    parser.add_argument("--require-all", action="store_true")
    args = parser.parse_args()

    paper_rows = _read_csv(args.paper)
    fieldnames = list(paper_rows[0].keys()) if paper_rows else []
    updated = merge_rows(paper_rows, _read_csv(args.formal), require_all=args.require_all)
    out_path = args.out or args.paper
    _write_csv(out_path, paper_rows, fieldnames)
    print(f"Updated {updated} HGPRA transfer cells in {out_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
